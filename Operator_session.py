# Here are short notes on operators in Python:

# Arithmetic Operators: Used for basic mathematical operations like addition (+), subtraction (-), multiplication (*), division (/), modulus (%), exponentiation (**), and floor division (//).

# Comparison Operators: Used to compare values. Includes equal to (==), not equal to (!=), greater than (>), less than (<), greater than or equal to (>=), and less than or equal to (<=).

# Assignment Operators: Used to assign values to variables. Includes simple assignment (=), compound assignment operators like +=, -=, *=, /=, %=, **=, and //=.

# Logical Operators: Used to combine conditional statements. Includes and, or, and not.

#Arithmetic Operators
x = 5
y = 2

print(x+y)
print(x/y)
print(x%y)
print(x//y)
print(x**y)
print(238 % 10)

print(4 % 2 + 3 - (2 * 3) / 2 % 4)
print(4 ** 2 / 4 - 2 % 3)
print(int(True) + 4 - (3 * int(False)))
print(3 + 5 / 2 * 4 % 5)
print((2 + 4) % 3 * 2 + 5 - 2 % 3)

